package app;

public class RideEntity {
	String riderNo;
	String time;
	String tag;
	String timePresentation;
	String group;
	int id;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getGroup() {
		return group;
	}

	public void setGroup(String group) {
		this.group = group;
	}

	public String getTimePresentation() {
		return timePresentation;
	}

	public void setTimePresentation(String timePresentation) {
		this.timePresentation = timePresentation;
	}

	public String getRiderNo() {
		return riderNo;
	}

	public void setRiderNo(String riderNo) {
		this.riderNo = riderNo;
	}

	public String getTime() {
		return time;
	}

	public void setTime(String time) {
		this.time = time;
	}

	public String getTag() {
		return tag;
	}

	public void setTag(String tag) {
		this.tag = tag;
	}

}
